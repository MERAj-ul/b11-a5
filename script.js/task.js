document.getElementById('btn1').addEventListener('click', function () {
        alert('Board update successfully');
        // Task assign
        const taskAssigned = document.getElementById('task-assigned').innerText;
        const taskRemain = parseInt(taskAssigned) - 1;
        document.getElementById('task-assigned').innerText = taskRemain;
        // Total Task
        const taskCount = document.getElementById('task-count').innerText;
        const totalTask = parseInt(taskCount) + 1;
        document.getElementById('task-count').innerText = totalTask;
    })

